# Python is a case sensitive language
print('Hello World')

print('fundamentals of python')

#print(fundamentals of python ) ----> You will get an error as strings are always written inside quotes. 


# Numbers can be printed without inverted commas !
print(10)
print(8)

# Boolean 
print(True)
print(False)

#Floating numbers
print(5.2)
print(6.3)

# You can also use multiple data types inside a same print ()
print('Hello',1,4.5,True)
print('Hello',1,4.5,True,sep='/')

# by default end value is set to a new line 
print('hello')
print('world')

#end ='-'
print('hello',end='-')
